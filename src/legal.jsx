// ── 아주 가벼운 마크다운 렌더러 (제목/목록/표/굵게/구분선) ──────
function inline(text, keyBase) {
  // **굵게** 처리
  const parts = text.split(/(\*\*[^*]+\*\*)/g);
  return parts.map((p, i) =>
    /^\*\*[^*]+\*\*$/.test(p)
      ? <b key={keyBase + "b" + i}>{p.slice(2, -2)}</b>
      : <React.Fragment key={keyBase + "t" + i}>{p}</React.Fragment>
  );
}

function splitRow(line) {
  return line.split("|").slice(1, -1).map((c) => c.trim());
}

function renderMarkdown(md) {
  const lines = md.split("\n");
  const out = [];
  let i = 0, k = 0;
  while (i < lines.length) {
    const line = lines[i];

    // 표
    if (/^\s*\|/.test(line) && i + 1 < lines.length && /^\s*\|[\s:|-]+\|?\s*$/.test(lines[i + 1])) {
      const header = splitRow(line);
      const rows = [];
      i += 2;
      while (i < lines.length && /^\s*\|/.test(lines[i])) { rows.push(splitRow(lines[i])); i++; }
      out.push(
        <div key={k++} style={LS.tableWrap}>
          <table style={LS.table}>
            <thead><tr>{header.map((h, j) => <th key={j} style={LS.th}>{h}</th>)}</tr></thead>
            <tbody>{rows.map((r, ri) => <tr key={ri}>{r.map((c, ci) => <td key={ci} style={LS.td}>{inline(c, `${ri}-${ci}-`)}</td>)}</tr>)}</tbody>
          </table>
        </div>
      );
      continue;
    }
    if (/^###\s/.test(line)) { out.push(<h3 key={k++} style={LS.h3}>{line.replace(/^###\s/, "")}</h3>); i++; continue; }
    if (/^##\s/.test(line)) { out.push(<h2 key={k++} style={LS.h2}>{line.replace(/^##\s/, "")}</h2>); i++; continue; }
    if (/^#\s/.test(line)) { out.push(<h1 key={k++} style={LS.h1}>{line.replace(/^#\s/, "")}</h1>); i++; continue; }
    if (/^---\s*$/.test(line)) { out.push(<hr key={k++} style={LS.hr} />); i++; continue; }

    // 목록 (번호/불릿, 들여쓰기 유지)
    if (/^\s*(\d+\.|-)\s/.test(line)) {
      const items = [];
      while (i < lines.length && /^\s*(\d+\.|-)\s/.test(lines[i])) {
        const indent = (lines[i].match(/^\s*/)[0] || "").length;
        const marker = lines[i].match(/^\s*(\d+\.|-)/)[1];
        const text = lines[i].replace(/^\s*(\d+\.|-)\s/, "");
        items.push({ indent, marker, text });
        i++;
      }
      out.push(
        <div key={k++} style={LS.list}>
          {items.map((it, j) => (
            <div key={j} style={{ ...LS.li, paddingLeft: 8 + it.indent * 12 }}>
              <span style={LS.marker}>{it.marker === "-" ? "•" : it.marker}</span>
              <span>{inline(it.text, `li${j}-`)}</span>
            </div>
          ))}
        </div>
      );
      continue;
    }
    if (line.trim() === "") { i++; continue; }
    out.push(<p key={k++} style={LS.p}>{inline(line, `p${k}-`)}</p>);
    i++;
  }
  return out;
}

