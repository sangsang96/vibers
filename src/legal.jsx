export function LegalModal({ doc, onClose }) {
  useEffect(() => {
    const esc = (e) => e.key === "Escape" && onClose();
    window.addEventListener("keydown", esc);
    const prev = document.body.style.overflow;
    document.body.style.overflow = "hidden";
    return () => { window.removeEventListener("keydown", esc); document.body.style.overflow = prev; };
  }, [onClose]);
  const md = doc === "privacy" ? PRIVACY : TERMS;
  const title = doc === "privacy" ? "개인정보처리방침" : "이용약관";
  return (
    <div style={LS.bg} onClick={onClose}>
      <div style={LS.modal} onClick={(e) => e.stopPropagation()}>
        <div style={LS.head}>
          <span style={LS.headTitle}>{title}</span>
          <button onClick={onClose} style={LS.close} aria-label="닫기">✕</button>
        </div>
        <div style={LS.body}>{renderMarkdown(md)}</div>
      </div>
    </div>
  );
}

